def replace_with_nearest_smaller(b):
    if len(b) < 1:
        return None
    n = len(b)
    result = [-2] * n  
    
    for i in range(n - 1):
        for j in range(i + 1, n):
            if b[j] < b[i]:
                result[i] = b[j]
                break
    
    return result


